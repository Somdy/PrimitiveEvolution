import java.util.List;

import static rs.primitiveevolution.datas.BranchID.*;

public class WallopEvo extends Evolution {
    public static int branchID(AbstractCard card) {
        int chosenBranch = -1;
        if (card instanceof EvolvableCard)
            chosenBranch = ((EvolvableCard) card).chosenBranch();
        switch (chosenBranch) {
            case 1:
                return Wallop_Light;
            case 2:
                return Wallop_Heavy;
            default:
                return 0;
        }
    }

    @NotNull
    @Contract("_ -> new")
    public static List<UpgradeBranch> branches(AbstractCard card) {
        return new ArrayList<UpgradeBranch>() {{
            add(() -> {
                upgradeName(card);
                upgradeDamage(card, 3);
            });
            add(() -> {
                upgradeEvolvedTexts(card, Wallop_Light);
                setBaseCost(card,1);
                setDamage(card,6);
            });
            add(() -> {
                upgradeEvolvedTexts(card, Wallop_Heavy);
                upgradeDamage(card,7);
                setMagic(card,2);
                card.exhaust = true;
            });
        }};
    }

    public static void upgrade(AbstractCard card) {
        if (card instanceof EvolvableCard)
            ((EvolvableCard) card).possibleBranches().get(((EvolvableCard) card).chosenBranch()).upgrade();
    }

    @SpirePatch(clz = Wallop.class, method = SpirePatch.CONSTRUCTOR)
    public static class Evolve {
        @SpireRawPatch
        public static void Raw(CtBehavior ctMethodToPatch) throws Exception {
            if (!manipulateMethods(WallopEvo.class, ctMethodToPatch))
                Nature.Log("Wallop is not evolvable.");
        }
    }

    @SpirePatch(clz = Wallop.class, method = "upgrade")
    public static class Upgrade {
        @SpirePrefixPatch
        public static SpireReturn Prefix(AbstractCard _inst) {
            if (_inst instanceof EvolvableCard && !_inst.upgraded) {
                ((EvolvableCard) _inst).possibleBranches().get(((EvolvableCard) _inst).chosenBranch()).upgrade();
                return SpireReturn.Return(null);
            }
            return SpireReturn.Continue();
        }
    }

    @SpirePatch(clz = Wallop.class, method = "use")
    public static class Use {
        @SpirePrefixPatch
        public static SpireReturn Prefix(AbstractCard _inst, AbstractPlayer p, AbstractMonster m) {
            if (_inst instanceof EvolvableCard && _inst.upgraded) {
                switch (((EvolvableCard) _inst).evolanch()) {
                    case Wallop_Light:
                        addToBot(new WallopAction(m, new DamageInfo(p, _inst.damage, _inst.damageTypeForTurn)));
                        return SpireReturn.Return(null);
                    case Wallop_Heavy:
                        addToBot(new WallopAction(m, new DamageInfo(p, _inst.damage, _inst.damageTypeForTurn)));
                        addToBot(new ApplyPowerAction(p, p, new BlurPower(p, _inst.magicNumber), _inst.magicNumber));
                        return SpireReturn.Return(null);
                }
            }
            return SpireReturn.Continue();
        }
    }
}
